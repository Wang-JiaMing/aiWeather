class weatherWarning:
    warnTitle = ''
    warnDate = ''
    warnContent = ''
    warnAnalysis = ''
    warnTips = ''

    def __init__(self, warnTitle, warnDate, warnContent, warnAnalysis, warnTips):
        self.warnTitle = warnTitle
        self.warnDate = warnDate
        self.warnContent = warnContent
        self.warnAnalysis = warnAnalysis
        self.warnTips = warnTips
